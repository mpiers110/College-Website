<p><span style="font-size:16px;"><span style="color:#B22222;"><strong>The Exam Officer Remarks</strong> </span></span></p>
<p style="text-align: justify;"><span style="font-size:16px;">Welcome to the examinations office of Kendege Technical and Vocational College. The office has the core responsibility of providing examination dates, keeping exam records of all departments, registration of candidates for exams and communicating with the relevant exam agents on all matters pertaining to exams. The exams department is mandated to coordinate the internal and external exams conducted in the college. It is obliged to providing quality internal exams to nurture the students&rsquo; critical &amp; analytical thinking and academic potential.</span></p>
<p style="text-align: justify;"><span style="font-size:16px;">The office ensures that the registration of students for the external exams is carried out precisely in the guidelines provided by the examining bodies. The students are expected to familiarize themselves with the examination manual. Internal exams comprise of two categories: continuous assessment tests (CAT) and ordinary exams. CAT may take any or a combination of practical/assignments or formal tests while ordinary exams are taken at the end of the term/semester. Only Kendege TVC students will be allowed to sit for ordinary exams at the college and one must have at least a 75% class attendance. Any student who is unable to sit for the exams should report to their respective HOD in writing before sitting for the exam citing reasons for the same to undergo approval process. Any case reported after the examinations will not be considered for examination appeals.</span><br />
&nbsp;</p>

<p style="text-align: justify;"><span style="font-size:16px;">Academic misconduct is not acceptable at all costs for both internal and external exams. Irregularities may take the form of impersonation, plagiarism, cheating, fabrication of results and collusion. After declaration of the exam results, any student who is unsatisfied with the results may appeal to the principal.</span><br />
<br />
<strong><i><span style="font-size:16px;"><span style="color:#B22222;">EXAMINING BODIES</span></span></i></strong><br />
<span style="font-size:16px;">1. Kendege Technical and Vocational College&rsquo;s internal exams<br />
2. Kenya National Examinations Council - KNEC<br />
3. Kenya Accountants and Secretaries National Examinations Board - KASNEB<br />
4. National Industrial Training Institute- NITA<br />
5. TVET Curriculum Development Assessment and Certification Council &ndash;TVET CDACC</span></p>

